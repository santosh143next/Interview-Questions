/* Function to mergesort the linked list
   It should return head of the modified list  */
   
Node* mergeSort(Node* head) {
    // your code here
    Node* curr = head;
    for(Node* i=curr;i!=NULL;i=i->next)
    {
        for(Node* j=i->next;j!=NULL;j=j->next)
        {
            if(j->data<i->data)
            {
                int temp = j->data;
                j->data = i->data;
                i->data = temp;
            }
        }
    }
    
    
    
    return head;
    
}


